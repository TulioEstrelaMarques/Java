    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        pnlR.setVisible(true);
        int a = Integer.parseInt(txtA.getValue().toString()),
            b = Integer.parseInt(txtB.getValue().toString()),
            c = Integer.parseInt(txtC.getValue().toString());
        double delta = Math.pow(b, 2)-4*a*c;
        lblResul.setText(String.format("%.2f", delta));
        if (delta>0) {
            lblResp.setText("Existem ra√≠zes Reais");
        } else {
            lblResp.setText("N√£o existem ra√≠zes Reais");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

